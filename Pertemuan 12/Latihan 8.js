// pernyataan if :
let angka = 2;

if (angka > 0) {
    console.log("Angka positif");
}

// Pernyataan If-Else:
let angka = -2;

if (angka > 0) {
    console.log("Angka positif");
} else {
    console.log("Angka negatif atau nol");
}

// pernyataan if-else if-else:
let nilai = 75;

if (nilai >= 90) {
    console.log("A");
} else if (nilai >= 80) {
    console.log("B");
} else if (nilai >= 70) {
    console.log("C");
} else if (nilai >= 60) {
    console.log("D");
} else {
    console.log("E");
}